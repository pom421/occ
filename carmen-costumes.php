							<p>Dans cette production de Carmen, la tradition est mise à mal : pas de robe espagnole, pas de beau toréador. Le soleil tape,
comme à Séville, mais il chauffe à blanc le bidonville. Les costumes, <b>sans âge et sans situation géographique</b>, répondent à la
nécessité. Ils sont <b>blancs, pour renvoyer les rayons brûlants</b>, ils sont sales parce que portés par des gens
démunis. Ce qui ne veut pas dire que la tentation d’être beau soit écartée.<br />
Bien qu’adaptées au labeur, les robes ont leur élégance, celle que l’on trouve chez les femmes noires simplement drapées
dans une bande de tissu. La décharge la plus proche, dûment fouillée, a fourni les <b>capsules de bouteilles, les boutons dépareillés,
les éclats de plastique qui ornent les décolletés, les ourlets</b>. Et chaque femme possède sa bande de tissu coloré, sa
richesse, transmise de mère en fille, offerte en cadeau de mariage, qu’elle utilise pour se couvrir, pour porter son
enfant, son fagot, pour se draper la tête ou juste jetée sur l’épaule avec coquetterie.<br />
Les hommes, également en blanc, le pantalon au mollet pour éviter de le déchirer dans les champs, portent eux aussi
cette pièce de tissu coloré, nouée autour de la taille ou jetée sur l’épaule.<br />
Avec un code vestimentaire si étroit, les bohémiennes se détachent aisément, par leurs bijoux, leurs jupes évasées,
leurs cheveux dénoués.<br />
Quant à la soldatesque, un rien suffit à marquer l’uniforme : le pantalon est long, la chemise porte un galon. La
misère est proche mais le respect de l’uniforme s’impose.<br />
Se jouant d’eux, les contrebandiers se fondent aux rochers bruns, rouille. Eux connaissent le froid des montagnes. Péons comme les autres, ils ont jeté sur leurs épaules des manteaux passe-muraille, épais et sombres.</p>
