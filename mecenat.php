					Ils se sont fixé plusieurs objectifs : 
<ul><li>rendre l’opéra accessible à tous en pratiquant une politique de prix attractive.</li>
<li>Permettre aux municipalités et aux directeurs de salles de programmer des spectacles   de   grande   qualité   dans   le cadre de budgets maitrisés.<br />
